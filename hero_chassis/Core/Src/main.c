void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	
	if (htim == &htim2)
    {			
		time++;
		
		
		
		if(time % 2 == 0)
		{
			chassis_feedback_update(&chassis_move);
			chassis_set_contorl(&chassis_move);
			chassis_control_loop(&chassis_move);		
			chassis_power_control(&chassis_move);
			if(channel[0].sR != 2)
			{
				CAN_cmd_chassis(chassis_move.motor_chassis[0].give_current, chassis_move.motor_chassis[1].give_current,
				chassis_move.motor_chassis[2].give_current, chassis_move.motor_chassis[3].give_current);			
			}
			
			if(channel[0].sR == 2)
			{
				CAN_cmd_chassis(0,0,0,0);			
			}			
		}
        
			trigger_feedback_update(&trigger_move);		
			trigger_control(&trigger_move);
			if(channel[0].sR != 2)
			{
				CAN_cmd_trigger(trigger_move.motor_trigger.give_current);			
			}
			if(channel[0].sR == 2)
			{
				CAN_cmd_trigger(0);			
			}			


		if(time % 1 == 0)
		{			
			gimbal_feedback_update(&gimbal_move);
			gimbal_control(&gimbal_move);		
			if(channel[0].sR != 2)
			{
				CAN_cmd_yaw(gimbal_move.motor_yaw.give_current);			
			}
			if(channel[0].sR == 2)
			{
				CAN_cmd_yaw(0);			
			}			
		}

	    if(time>1000)
		{
			time = 0;
		}
				
	}
		
}
